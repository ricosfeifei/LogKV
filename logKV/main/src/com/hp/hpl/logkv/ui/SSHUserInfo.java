package com.hp.hpl.logkv.ui;

import com.jcraft.jsch.UserInfo;

public class SSHUserInfo implements UserInfo {
	
	public static final String DEFAULT_HOST = "localhost";
	public static final String DEFAULT_USER = System.getProperty("user.name");
	
	private String host = SSHUserInfo.DEFAULT_HOST;
	private String user = SSHUserInfo.DEFAULT_USER;
	private String uid;
	private String password; 
	
	
	/**
	 * 
	 */
	public SSHUserInfo() {
		super();
	}
	
	/**
	 * @param host
	 * @param user
	 * @param password
	 */
	public SSHUserInfo(String host, String user, String password) {
		super();
		this.host = host;
		this.user = user;
		this.password = password;
	}

	@Override
	public String getPassphrase() {
		return null;
	}

	@Override
	public String getPassword() {
		return password;
	}

	@Override
	public boolean promptPassphrase(String arg0) {
		return true;
	}

	@Override
	public boolean promptPassword(String arg0) {
		return true;
	}

	@Override
	public boolean promptYesNo(String arg0) {
		return true;
	}

	@Override
	public void showMessage(String arg0) {
	}

	/**
	 * @return the host
	 */
	public String getHost() {
		return host;
	}

	/**
	 * @param host the host to set
	 */
	public void setHost(String host) {
		this.host = host;
	}

	/**
	 * @return the user
	 */
	public String getUser() {
		return user;
	}

	/**
	 * @param user the user to set
	 */
	public void setUser(String user) {
		this.user = user;
	}

	/**
	 * @param password the password to set
	 */
	public void setPassword(String password) {
		this.password = password;
	}

	/**
	 * @return the uid
	 */
	public String getUid() {
		return uid;
	}

	/**
	 * @param uid the uid to set
	 */
	public void setUid(String uid) {
		this.uid = uid;
	}
}
